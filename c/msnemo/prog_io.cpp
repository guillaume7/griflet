#include "stdafx.h"

//*************************************************************************************
void readint(unsigned int *i){
//*************************************************************************************
	scanf("%d", i);
	fflush(stdin);
	return;
}

//*************************************************************************************
void readfloat(double *x){
//*************************************************************************************
	char string[7];
	scanf("%7s", string);
	fflush(stdin);
	*x=atof(string);
	return;
}

//*************************************************************************************
void freadfloat(double *x, FILE *file){
//*************************************************************************************
	char string[17];
	fscanf(file,"%s", string);
	*x=atof(string);
	return;
}
